def check_exaggeration(text: str):
    """
    ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÇ‡∏Ü‡∏©‡∏ì‡∏≤‡πÄ‡∏Å‡∏¥‡∏ô‡∏à‡∏£‡∏¥‡∏á (Main Scanner)
    ------------------------------------------
    Input: ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÇ‡∏Ü‡∏©‡∏ì‡∏≤ (str)
    Output: (is_bad: bool, results: list)
    
    ‡∏´‡∏ô‡πâ‡∏≤‡∏ó‡∏µ‡πà:
    1. ‡∏£‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏Ç‡πâ‡∏≤‡∏°‡∏≤ ‡πÅ‡∏•‡πâ‡∏ß‡πÅ‡∏¢‡∏Å‡πÄ‡∏õ‡πá‡∏ô‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ
    2. ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ó‡∏µ‡∏•‡∏∞‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ‡∏ß‡πà‡∏≤‡∏°‡∏µ‡∏Ñ‡∏≥‡∏ï‡πâ‡∏≠‡∏á‡∏´‡πâ‡∏≤‡∏°‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏´‡∏°
    3. ‡∏ñ‡πâ‡∏≤‡πÄ‡∏à‡∏≠ ‡πÉ‡∏´‡πâ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ß‡πà‡∏≤‡πÄ‡∏à‡∏≠‡∏Ñ‡∏≥‡∏ß‡πà‡∏≤‡∏≠‡∏∞‡πÑ‡∏£, ‡∏ú‡∏¥‡∏î‡πÄ‡∏û‡∏£‡∏≤‡∏∞‡∏≠‡∏∞‡πÑ‡∏£, ‡πÅ‡∏•‡∏∞‡∏£‡∏∏‡∏ô‡πÅ‡∏£‡∏á‡πÅ‡∏Ñ‡πà‡πÑ‡∏´‡∏ô
    """

    bad_sentences = []
    sentences = split_sentences(text)
    
    # ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ó‡∏µ‡∏•‡∏∞‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ
    for sentence in sentences:
        found_words = []
        reasons = []
        risk_categories = []
        
        # ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÄ‡∏ä‡πá‡∏Ñ‡∏Ñ‡∏≥‡∏®‡∏±‡∏û‡∏ó‡πå‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏≥‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• (ALL_RULES)
        for keyword, reason in ALL_RULES.items():
            if keyword in sentence:
                found_words.append(keyword)
                reasons.append(reason)
                
                # ‡πÄ‡∏ä‡πá‡∏Ñ‡∏£‡∏∞‡∏î‡∏±‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏£‡∏∏‡∏ô‡πÅ‡∏£‡∏á‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏à‡∏±‡∏î‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà
                severity = get_risk_level(keyword)
                risk_label = "‡∏ú‡∏¥‡∏î‡∏Å‡∏é‡∏£‡πâ‡∏≤‡∏¢‡πÅ‡∏£‡∏á" if severity == 'violation' else "‡πÇ‡∏Ü‡∏©‡∏ì‡∏≤‡πÄ‡∏Å‡∏¥‡∏ô‡∏à‡∏£‡∏¥‡∏á"
                risk_categories.append(risk_label)

        # ‡∏ñ‡πâ‡∏≤‡πÄ‡∏à‡∏≠‡∏Ñ‡∏≥‡∏ú‡∏¥‡∏î‡πÉ‡∏ô‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ‡∏ô‡∏±‡πâ‡∏ô ‡πÉ‡∏´‡πâ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏•‡∏á list
        if found_words:
            # ‡∏•‡∏ö‡∏Ñ‡∏≥‡∏ã‡πâ‡∏≥ (‡πÄ‡∏ú‡∏∑‡πà‡∏≠‡πÄ‡∏à‡∏≠ rule ‡∏ã‡πâ‡∏≠‡∏ô‡∏Å‡∏±‡∏ô)
            risk_categories = list(set(risk_categories))
            
            bad_sentences.append({
                "sentence": sentence,          # ‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ‡∏ó‡∏µ‡πà‡∏ú‡∏¥‡∏î
                "words": found_words,          # ‡∏Ñ‡∏≥‡∏ó‡∏µ‡πà‡∏ú‡∏¥‡∏î (‡πÄ‡∏ä‡πà‡∏ô "‡∏´‡∏≤‡∏¢‡∏Ç‡∏≤‡∏î")
                "reasons": reasons,            # ‡πÄ‡∏´‡∏ï‡∏∏‡∏ú‡∏• (‡πÄ‡∏ä‡πà‡∏ô "‡∏≠‡∏¢. ‡∏´‡πâ‡∏≤‡∏°‡∏£‡∏±‡∏ö‡∏£‡∏≠‡∏á‡∏ú‡∏•")
                "risk_categories": risk_categories, # ‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ú‡∏¥‡∏î
                # *‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç* ‡πÄ‡∏£‡∏≤‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÉ‡∏™‡πà key 'severity' ‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ 
                # ‡πÄ‡∏û‡∏£‡∏≤‡∏∞ main.py ‡∏à‡∏∞‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÉ‡∏´‡∏°‡πà‡∏à‡∏≤‡∏Å words ‡πÄ‡∏≠‡∏á (‡∏´‡∏£‡∏∑‡∏≠‡∏à‡∏∞‡πÉ‡∏™‡πà‡πÑ‡∏õ‡πÄ‡∏•‡∏¢‡∏Å‡πá‡πÑ‡∏î‡πâ ‡πÅ‡∏ï‡πà main.py ‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î‡∏°‡∏µ logic ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ã‡πâ‡∏≥‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß)
            })

    # ‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ (True ‡∏ñ‡πâ‡∏≤‡πÄ‡∏à‡∏≠‡∏Ñ‡∏≥‡∏ú‡∏¥‡∏î, ‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡∏Ñ‡∏≥‡∏ú‡∏¥‡∏î)
    return bool(bad_sentences), bad_sentences