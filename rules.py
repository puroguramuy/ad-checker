    return 'risk'           # ‡πÅ‡∏Ñ‡πà‡πÇ‡∏Ü‡∏©‡∏ì‡∏≤‡πÄ‡∏Å‡∏¥‡∏ô‡∏à‡∏£‡∏¥‡∏á (‡∏õ‡∏£‡∏±‡∏ö‡∏õ‡∏£‡∏∏‡∏á‡πÑ‡∏î‡πâ)

